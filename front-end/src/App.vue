<script>
import { API_BASE_URL } from './config.js'
import FileUpload from './components/FileUpload.vue'
import MarkdownRenderer from './components/MarkdownRenderer.vue'
import Toast from './components/Toast.vue'
import HeroBanner from './components/HeroBanner.vue'
import SampleSummary from './components/SampleSummary.vue'

export default {
  name: 'App',
  components: {
    FileUpload,
    MarkdownRenderer,
    Toast,
    HeroBanner,
    SampleSummary
  },
  data() {
    return {
      file: null,
      fileResetKey: 0,
      summary: '',
      loading: false,
      error: '',
      elapsed: 0,
      year: new Date().getFullYear(),
      theme: 'light',
      model: '',
      models: [],
      apiBaseUrl: API_BASE_URL,
      showToast: false,
      toastType: 'info',
      toastMessage: '',
      showSampleModal: false,
      _timer: null,
      _es: null,
      _toastTimer: null,
      _hasScrolledToSummary: false
    }
  },
  computed: {
    isDark() {
      return this.theme === 'dark'
    },
    fileName() {
      return this.file ? this.file.name : ''
    },
    structuredSummary() {
      if (!this.summary) return ''
      return this.formatSummaryForDisplay(this.summary)
    }
  },
  mounted() {
    this.theme = window.matchMedia('(prefers-color-scheme: dark)').matches ? 'dark' : 'light'
    document.documentElement.dataset.theme = this.theme
    this.loadModels()
  },
  beforeUnmount() {
    this._clearTimer()
    this._closeES()
  },
  methods: {
    async loadModels() {
      try {
        const response = await fetch(`${this.apiBaseUrl}/models`)
        if (!response.ok) return
        const payload = await response.json()
        this.models = payload.models || []
        this.model = payload.default || this.models[0] || ''
      } catch (error) {
        console.warn('تعذر تحميل النماذج', error)
      }
    },
    setFile(file) {
      this.file = file
      this.error = ''
    },
    toggleTheme() {
      const nextTheme = this.isDark ? 'light' : 'dark'
      this.theme = nextTheme
      document.documentElement.dataset.theme = this.theme
      this.notify(nextTheme === 'dark' ? 'تم تفعيل الوضع الداكن' : 'تم تفعيل الوضع الفاتح', 'success')
    },
    formatElapsed(seconds) {
      const minutes = Math.floor(seconds / 60)
        .toString()
        .padStart(2, '0')
      const remainder = (seconds % 60)
        .toString()
        .padStart(2, '0')
      return `${minutes}:${remainder}`
    },
    async summarize() {
      if (!this.file) {
        this.notify('⚠️ اختر ملف PDF أولاً!', 'error')
        return
      }

      this._closeES()
      this._clearTimer()

      this.summary = ''
      this.error = ''
      this.loading = true
      this.elapsed = 0
      this._hasScrolledToSummary = false

      this._timer = setInterval(() => {
        this.elapsed += 1
      }, 1000)

      try {
        const formData = new FormData()
        formData.append('file', this.file)

        const uploadResponse = await fetch(`${this.apiBaseUrl}/upload`, {
          method: 'POST',
          body: formData
        })

        if (!uploadResponse.ok) {
          throw new Error('فشل رفع الملف')
        }

        const uploadData = await uploadResponse.json()
        const sessionId = uploadData.session_id

        if (!sessionId) {
          throw new Error('تعذر بدء جلسة التلخيص')
        }

        const url = `${this.apiBaseUrl}/summarize-gemini?session_id=${encodeURIComponent(
          sessionId
        )}&model=${encodeURIComponent(this.model)}`

        const es = new EventSource(url)
        this._es = es

        es.onmessage = event => {
          if (!event.data) return
          this.summary += event.data
          if (this.summary && !this._hasScrolledToSummary) {
            this.scrollToSummary()
            this._hasScrolledToSummary = true
          }
        }

        es.addEventListener('status', event => {
          if (event.data === 'DONE') {
            this.loading = false
            this.notify('اكتمل التلخيص', 'success')
            this._closeES()
            this._clearTimer()
            this.scrollToSummary()
          }
        })

        es.addEventListener('error', () => {
          this.loading = false
          this.notify('تعذر إكمال التلخيص', 'error')
          this._closeES()
          this._clearTimer()
        })
      } catch (error) {
        this.loading = false
        this.notify(error.message || 'تعذر رفع الملف', 'error')
        this._clearTimer()
        this._closeES()
      }
    },
    cancel() {
      if (!this.loading) return
      this.loading = false
      this.notify('تم إيقاف عملية التلخيص', 'info')
      this._clearTimer()
      this._closeES()
      this._hasScrolledToSummary = false
    },
    reset() {
      this._closeES()
      this._clearTimer()
      this.summary = ''
      this.error = ''
      this.file = null
      this.fileResetKey += 1
      this.loading = false
      this._hasScrolledToSummary = false
      this.notify('تم بدء جلسة جديدة', 'success')
    },
    notify(message, type = 'info') {
      this.toastMessage = message
      this.toastType = type
      this.showToast = true

      if (type === 'error') {
        this.error = message
      }

      clearTimeout(this._toastTimer)
      this._toastTimer = setTimeout(() => {
        this.showToast = false
        if (type !== 'error') {
          this.error = ''
        }
      }, 4000)
    },
    copySummary() {
      if (!this.summary) return
      navigator.clipboard.writeText(this.summary)
      this.notify('تم النسخ', 'success')
    },
    downloadSummary() {
      if (!this.summary) return
      const blob = new Blob([this.summary], { type: 'text/plain;charset=utf-8' })
      const link = document.createElement('a')
      link.href = URL.createObjectURL(blob)
      link.download = 'summary.txt'
      link.click()
      URL.revokeObjectURL(link.href)
      this.notify('تم التنزيل', 'success')
    },
    scrollToUpload() {
      const element = this.$refs.uploadCard
      if (!element) return
      element.scrollIntoView({ behavior: 'smooth', block: 'start' })
    },
    scrollToSummary() {
      const element = this.$refs.conversationSection
      if (!element) return
      element.scrollIntoView({ behavior: 'smooth', block: 'start' })
    },
    openSample() {
      this.showSampleModal = true
    },
    closeSample() {
      this.showSampleModal = false
    },
    _closeES() {
      if (this._es) {
        this._es.close()
        this._es = null
      }
    },
    _clearTimer() {
      if (this._timer) {
        clearInterval(this._timer)
        this._timer = null
      }
    },
    formatSummaryForDisplay(text) {
      if (!text) return ''

      const hasRichMarkdown = /(\n\s*\n)|(^|\n)\s*(?:[-*+]\s|\d+\.\s|>\s)/.test(text)
      if (hasRichMarkdown) {
        return text
      }

      const sanitized = text
        .replace(/[\u2022\u2023\u25E6\u2043\u2219]+/g, '•')
        .replace(/\s+\n/g, '\n')
        .replace(/([.!؟؛])(?=\S)/g, '$1 ')
        .trim()

      const normalized = sanitized
        .replace(/\s*(#{1,6}\s+)/g, '\n\n$1')
        .replace(/\n{3,}/g, '\n\n')

      let sentences = []
      if (typeof Intl !== 'undefined' && typeof Intl.Segmenter === 'function') {
        const segmenter = new Intl.Segmenter('ar', { granularity: 'sentence' })
        sentences = Array.from(segmenter.segment(normalized)).map(segment => segment.segment.trim())
      }

      if (!sentences.length) {
        const splitter = /(?<=[.!؟?؛])\s+/g
        sentences = normalized
          .split(splitter)
          .map(sentence => sentence.trim())
          .filter(Boolean)
      } else {
        sentences = sentences.filter(Boolean)
      }

      if (sentences.length <= 1) {
        const commaChunks = sanitized
          .split(/،+/)
          .map(chunk => chunk.trim())
          .filter(Boolean)

        if (commaChunks.length > 1) {
          const [lead, ...rest] = commaChunks
          const bulletLines = rest.map(chunk => `- ${chunk.replace(/^[\-•]+\s*/, '')}`)
          return [lead, ...bulletLines].join('\n\n')
        }

        return sanitized
      }

      const paragraphs = []
      let bucket = []

      sentences.forEach(sentence => {
        if (/^(?:•|-)/.test(sentence)) {
          if (bucket.length) {
            paragraphs.push(bucket.join(' '))
            bucket = []
          }
          const bulletLine = sentence.replace(/^(?:•|-)+\s*/, '- ')
          paragraphs.push(bulletLine)
          return
        }

        const colonBullet = sentence.match(/^([\u0600-\u06FFA-Za-z0-9 ()\/\-]{3,120}?):\*\*\s*(.+)$/)
        if (colonBullet) {
          if (bucket.length) {
            paragraphs.push(bucket.join(' '))
            bucket = []
          }
          paragraphs.push(`- **${colonBullet[1].trim()}:** ${colonBullet[2].trim()}`)
          return
        }

        bucket.push(sentence)
        const bucketText = bucket.join(' ')
        if (bucketText.length >= 160) {
          paragraphs.push(bucketText)
          bucket = []
        }
      })

      if (bucket.length) {
        paragraphs.push(bucket.join(' '))
      }

      const combined = []
      let bulletBuffer = []

      paragraphs.forEach(paragraph => {
        const trimmed = paragraph.trim()
        if (trimmed.startsWith('- ')) {
          bulletBuffer.push(trimmed)
        } else {
          if (bulletBuffer.length) {
            combined.push(bulletBuffer.join('\n'))
            bulletBuffer = []
          }
          combined.push(trimmed)
        }
      })

      if (bulletBuffer.length) {
        combined.push(bulletBuffer.join('\n'))
      }

      const formatted = combined.map(block => {
        if (/^##\s+/.test(block)) {
          return block
        }
        const colonBold = block.match(/^([\u0600-\u06FFA-Za-z0-9 ()\/\-]{3,120}?):\*\*\s*(.+)$/)
        if (colonBold) {
          return `- **${colonBold[1].trim()}:** ${colonBold[2].trim()}`
        }
        return block
      })

      return formatted.join('\n\n')
    }
  }
}
